        public static string GetShortFriendlyMessage(PostgresException ex)
        {
            string errorType = ErrorCodeMessages.GetValueOrDefault(
                ex.SqlState,
                "数据库错误"
            );
            return $"错误：{errorType} ({ex.SqlState})";
        }

