        /// <summary>
        /// 获取错误码对应的简短描述
        /// </summary>
        public static string GetErrorCodeDescription(string sqlState)
        {
            return ErrorCodeMessages.GetValueOrDefault(sqlState, "未知错误");
        }

        /// <summary>
        /// 判断是否为可重试的错误
        /// </summary>
        public static bool IsRetryableError(PostgresException ex)
        {
            return ex.SqlState switch
            {
                // 连接相关错误
                "08006" or "08003" or "57P03" or "53300" => true,
                // 死锁和序列化错误
                "40001" or "40P01" => true,
                // 超时错误
                "57014" => true,
                // 其他临时性错误
                "53000" or "53100" or "53200" => true,
                _ => false
            };
        }

        /// <summary>
        /// 获取错误的严重级别
        /// </summary>
        public static ErrorSeverity GetErrorSeverity(PostgresException ex)
        {
            return ex.SqlState switch
            {
                // 致命错误
                "53" or "54" or "57" or "58" => ErrorSeverity.Critical,
                // 约束相关错误
                "23" => ErrorSeverity.Warning,
                // 语法错误
                "42" => ErrorSeverity.Error,
                // 其他
                _ => ErrorSeverity.Info
            };
        }
    }

    /// <summary>
    /// 错误严重级别
    /// </summary>
    public enum ErrorSeverity
    {
        Info,       // 信息
        Warning,    // 警告
        Error,      // 错误
        Critical    // 严重错误
    }
